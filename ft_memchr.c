#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t i;

	i = 0;
	while (s && i <= n)
	{
		if (((int *)s)[i] == c)
			return (((void *)s) + i);
		i++;
	}
	return (NULL);
}
