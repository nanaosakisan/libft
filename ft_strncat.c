#include "libft.h"

char    *ft_strncat(char *s1, const char *s2, size_t n)
{
    int i;
    size_t j;

    i = 0;
    j = 0;
    while(s1[i])
    {
        while(j >= n)
        {
            if(s1[i + 1] == '\0')
            {
                s1[i + 1] = s2[j];
                j++;
            }
            i++;
        }
    }
    return(s1);
}
